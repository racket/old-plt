static wxColor *the_color;

static void draw_scanline(JSAMPROW row, int cols, int rownum, JSAMPARRAY colormap, wxMemoryDC *dc)
{
  int colnum;

  if (!the_color) {
    wxREGGLOB(the_color);
    the_color = new wxColour(0, 0, 0);
  }

  for (colnum = 0; colnum < cols; colnum++) {
    int v;
    v = row[colnum];
    the_color->Set(colormap[0][v], colormap[1][v], colormap[2][v]);
    dc->SetPixel(colnum, rownum, the_color);
  }
}

wxMemoryDC *create_dc(int width, int height, wxBitmap *bm)
{
  wxMemoryDC *dc;

  dc = new wxMemoryDC();
  bm->Create(width, height);
  dc->SelectObject(bm);

  if (!dc->Ok())
    return NULL;

  return dc;
}

